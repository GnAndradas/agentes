export function agentTemplate(
  name: string,
  description: string,
  type: 'general' | 'specialist' | 'orchestrator',
  capabilities: string[]
): {
  name: string;
  description: string;
  type: 'general' | 'specialist' | 'orchestrator';
  capabilities: string[];
  config: Record<string, unknown>;
} {
  return {
    name,
    description,
    type,
    capabilities,
    config: {
      maxConcurrentTasks: type === 'orchestrator' ? 10 : 3,
      timeout: 300,
      retryOnError: true,
    },
  };
}
