import { createLogger } from '../utils/logger.js';
import { getServices } from '../services/index.js';
import { getAIClient, type AgentAIResponse, AIGenerationError } from './AIClient.js';
import { getValidator } from './Validator.js';
import { agentTemplate } from './templates/index.js';
import { ForbiddenError } from '../utils/errors.js';
import {
  canCreateAgentAutonomously,
  requiresApprovalForAgentCreation,
} from '../config/autonomy.js';
import {
  createTraceability,
  toStoredTraceability,
  toOriginMetadata,
  MATERIALIZATION_STATUS,
  type FullGenerationTraceability,
} from './traceability.js';
import {
  materializeAgent,
  computeMaterializationStatus,
  getStatusDescription,
  type MaterializationTraceability,
  type AgentMaterializationStatus,
} from './AgentMaterialization.js';
import type { GenerationRequest, GenerationOutput } from './types.js';
import type { AgentType } from '../types/domain.js';
import type { AIErrorStage } from '../types/contracts.js';

export type GenerationSource = 'api' | 'system' | 'orchestrator';

const logger = createLogger('AgentGenerator');

/**
 * Content stored in generation.generatedContent for agents
 * This is the source of truth used during activation
 */
interface AgentGeneratedContent {
  type: AgentType;
  capabilities: string[];
  config: Record<string, unknown>;
  generatedBy: 'ai' | 'template';
  /** BLOQUE 7: Trazabilidad explícita de generación */
  _traceability?: Record<string, unknown>;
  /** BLOQUE 9: Trazabilidad de materialización */
  _materialization_trace?: MaterializationTraceability;
  /** BLOQUE 9: Estado de materialización */
  _materialization_status?: AgentMaterializationStatus;
  [key: string]: unknown; // Allow index signature for Record<string, unknown> compatibility
}

export class AgentGenerator {
  async generate(request: GenerationRequest, source: GenerationSource = 'api'): Promise<GenerationOutput> {
    // Check autonomy permissions for non-human sources
    if (source !== 'api') {
      if (!canCreateAgentAutonomously()) {
        throw new ForbiddenError('Autonomous agent generation is disabled by autonomy policy');
      }
    }

    const { generationService } = getServices();
    const aiClient = getAIClient();
    const validator = getValidator();

    const generation = await generationService.create({
      type: 'agent',
      name: request.name,
      description: request.description,
      prompt: request.prompt,
    });

    try {
      let agentType: AgentType = 'general';
      let capabilities: string[] = [];
      let config: Record<string, unknown> = {};
      let generatedBy: 'ai' | 'template' = 'template';

      // BLOQUE 7: Build traceability throughout the flow
      const trace = createTraceability().aiRequested();

      // Helper to generate via template - returns the values
      const generateTemplate = (): { type: AgentType; capabilities: string[]; config: Record<string, unknown> } => {
        const template = agentTemplate(
          request.name,
          request.description,
          'specialist',
          request.requirements || []
        );
        return {
          type: template.type,
          capabilities: template.capabilities,
          config: template.config,
        };
      };

      const aiConfigured = aiClient.isConfigured();
      trace.aiAvailable(aiConfigured);

      if (aiConfigured) {
        // Try AI generation with user's prompt
        trace.aiAttempted();
        trace.aiRequestStarted();

        try {
          const response = await aiClient.generate<AgentAIResponse>({
            type: 'agent',
            name: request.name,
            description: request.description,
            prompt: request.prompt,
            requirements: request.requirements,
          });

          // PROMPT 19: Track detailed success state
          trace.aiReachedGateway(response.reachedGateway ?? true);
          trace.aiRawResponseReceived(response.rawResponseReceived ?? true, response.content?.substring(0, 200));

          // Validate parsed content has required fields
          if (!response.parsed.type) {
            trace.aiUnusableResponse('invalid_shape', 'AI response missing required "type" field');
            throw new Error('AI response missing type field');
          }

          agentType = response.parsed.type || 'general';
          capabilities = response.parsed.capabilities || [];
          config = response.parsed.config || {};
          generatedBy = 'ai';

          // PROMPT 16B: Record AI success with usage and runtime info
          trace.aiResult(true, 'claude', response.usage, 'openclaw', response.runtime);
          trace.aiContentUsable(true);

          logger.debug({
            name: request.name,
            type: agentType,
            capCount: capabilities.length,
          }, 'Agent generated by AI');
        } catch (aiErr) {
          // PROMPT 19: Capture detailed error info
          if (aiErr instanceof AIGenerationError) {
            trace.aiError(aiErr.errorType, aiErr.errorStage, aiErr.message, aiErr.errorCode);
            trace.aiReachedGateway(aiErr.errorStage !== 'gateway_unreachable' && aiErr.errorStage !== 'hooks_not_configured');
            trace.aiRawResponseReceived(
              aiErr.errorStage === 'parse_failed' || aiErr.errorStage === 'validator_failed' || aiErr.errorStage === 'invalid_shape',
              aiErr.rawResponseSnippet
            );
          } else {
            const errMsg = aiErr instanceof Error ? aiErr.message : 'Unknown error';
            let stage: AIErrorStage = 'unknown';
            if (errMsg.includes('missing') || errMsg.includes('type')) {
              stage = 'invalid_shape';
              trace.aiUnusableResponse(stage, errMsg);
            } else {
              trace.aiTechnicalError(stage, errMsg);
            }
          }

          trace.aiResult(false).usedFallback('ai_request_failed', 'agentTemplate');

          logger.warn({ name: request.name, err: aiErr }, 'AI generation failed, using template fallback');
          const tmpl = generateTemplate();
          agentType = tmpl.type;
          capabilities = tmpl.capabilities;
          config = tmpl.config;
          generatedBy = 'template';
        }
      } else {
        trace.usedFallback('ai_not_configured', 'agentTemplate');

        logger.info({ name: request.name }, 'Using template (AI not configured)');
        const tmpl = generateTemplate();
        agentType = tmpl.type;
        capabilities = tmpl.capabilities;
        config = tmpl.config;
        generatedBy = 'template';
      }

      // Validate the generated content
      const validation = validator.validateAgent({
        name: request.name,
        type: agentType,
        capabilities,
        config,
      });

      // Record validation result in traceability
      trace.validated(validation.valid, validation.errors, validation.warnings);

      if (!validation.valid) {
        await generationService.markFailed(generation.id, validation.errors.join('; '));
        return {
          files: [],
          metadata: { validation, generationId: generation.id, _traceability: toStoredTraceability(trace.build()) },
        };
      }

      // BLOQUE 7: Mark materialization gap explicitly
      trace.materialization(false, MATERIALIZATION_STATUS.agent.gap);

      // Store the full generated content for activation (with traceability)
      const generatedContent: AgentGeneratedContent = {
        type: agentType,
        capabilities,
        config,
        generatedBy,
        _traceability: toStoredTraceability(trace.build()),
      };

      // Extract origin metadata for frontend display
      const originMetadata = toOriginMetadata(trace.build());

      await generationService.markGenerated(
        generation.id,
        generatedContent,
        `agents/${request.name}`,
        originMetadata
      );

      // Always go to pending_approval first (required by FSM)
      await generationService.markPendingApproval(generation.id, {
        valid: true,
        warnings: validation.warnings,
        type: agentType,
        capabilitiesCount: capabilities.length,
        generatedBy,
      });

      logger.info({
        generationId: generation.id,
        name: request.name,
        type: agentType,
        generatedBy,
        requiresApproval: this.requiresApproval(source),
      }, 'Agent generation completed');

      return {
        files: [],
        metadata: {
          generationId: generation.id,
          type: agentType,
          capabilities,
          config,
          validation,
          generatedBy,
        },
      };
    } catch (err) {
      const message = err instanceof Error ? err.message : 'Unknown error';
      await generationService.markFailed(generation.id, message);
      throw err;
    }
  }

  async activate(generationId: string): Promise<void> {
    const { generationService, agentService } = getServices();

    const generation = await generationService.getById(generationId);

    if (generation.status !== 'approved') {
      throw new Error('Can only activate approved generations');
    }

    // Use the stored content directly (no regeneration)
    const content = generation.generatedContent as AgentGeneratedContent;

    if (!content || !content.type) {
      throw new Error('Missing or invalid generated content for agent');
    }

    // PROMPT 17: Extract linked tools/skills from metadata
    const meta = generation.metadata || {};
    const linkedTools: string[] = [];
    const linkedSkills: string[] = [];

    // Check for linked resources in metadata
    if (meta.linkedToolId) linkedTools.push(meta.linkedToolId as string);
    if (meta.tools && Array.isArray(meta.tools)) linkedTools.push(...(meta.tools as string[]));
    if (meta.linkedSkillId) linkedSkills.push(meta.linkedSkillId as string);
    if (meta.skills && Array.isArray(meta.skills)) linkedSkills.push(...(meta.skills as string[]));

    // BLOQUE 9 + PROMPT 17: Attempt real materialization (workspace + config files + tools/skills)
    const materializationTrace = await materializeAgent(
      generation.name,
      content.type,
      generation.description,
      content.capabilities,
      content.config,
      'activation',
      linkedTools.length > 0 ? linkedTools : undefined,
      linkedSkills.length > 0 ? linkedSkills : undefined
    );

    // Compute final materialization status
    const materializationStatus = computeMaterializationStatus(
      generation.name,
      true, // will have DB record after create
      true, // generation is being activated
      undefined // no OpenClaw session yet
    );

    // Create agent in DB using stored content
    // BLOQUE 9: Now includes actual materialization status
    const agent = await agentService.create({
      name: generation.name,
      description: generation.description,
      type: content.type,
      capabilities: content.capabilities,
      config: {
        ...content.config,
        // BLOQUE 9: Store full materialization status
        _materialization: {
          state: materializationStatus.state,
          db_record: true,
          workspace_exists: materializationStatus.workspace_exists,
          config_written: materializationStatus.config_written,
          runtime_possible: materializationStatus.runtime_possible,
          openclaw_session: false,
          materialization_attempted_at: materializationTrace.attempted_at,
          materialization_succeeded: materializationTrace.steps_failed.length === 0,
          materialization_reason: materializationTrace.gap,
          target_workspace: materializationStatus.target_workspace,
          // Keep BLOQUE 7 gap for compatibility
          gap: MATERIALIZATION_STATUS.agent.gap,
          activated_at: Date.now(),
        },
      },
    });

    // Update generation metadata with resourceId for frontend link
    await generationService.updateMetadata(generationId, {
      resourceId: agent.id,
      agentId: agent.id,
      resourceStatus: agent.status,
    });

    await generationService.activate(generationId);

    // BLOQUE 9: Explicit logging of real state
    logger.info({
      generationId,
      name: generation.name,
      type: content.type,
      generatedBy: content.generatedBy,
      // BLOQUE 9: Real materialization status
      lifecycle_state: materializationStatus.state,
      materialization: {
        attempted: true,
        succeeded: materializationTrace.steps_failed.length === 0,
        steps_completed: materializationTrace.steps_completed,
        steps_failed: materializationTrace.steps_failed,
        workspace_exists: materializationStatus.workspace_exists,
        config_written: materializationStatus.config_written,
        runtime_ready: false, // OpenClaw session not started
      },
      status_description: getStatusDescription(materializationStatus),
    }, 'Agent activated with materialization');
  }

  // Check if generation requires approval
  requiresApproval(source: GenerationSource): boolean {
    if (source === 'api') return false; // Human-initiated via API doesn't need approval
    return requiresApprovalForAgentCreation();
  }
}

let agentGeneratorInstance: AgentGenerator | null = null;

export function getAgentGenerator(): AgentGenerator {
  if (!agentGeneratorInstance) {
    agentGeneratorInstance = new AgentGenerator();
  }
  return agentGeneratorInstance;
}
