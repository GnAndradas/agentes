/**
 * Handle permission request
 */
async function handleRequestPermission(
  job: JobRecord,
  suggestion: BlockingSuggestion
): Promise<ResolutionResult> {
  const { approvalService } = getServices();

  const approval = await approvalService.create({
    type: 'permission',
    metadata: {
      agentId: job.payload.agent.agentId,
      permissionTarget: suggestion.target,
      description: suggestion.description,
      sourceJobId: job.id,
      onApprove: 'grant_and_retry',
    },
  });

  return {
    success: true,
    approvalId: approval.id,
    autoRetryScheduled: false,
  };
}

// ============================================================================
// RETRY SYSTEM
// ============================================================================

/**
 * Schedule automatic retry for a job
 */
export function scheduleRetry(
  jobId: string,
  delayMs: number,
  reason: string
): void {
  // Cancel any existing retry
  cancelPendingRetry(jobId);

  logger.info({ jobId, delayMs, reason }, 'Scheduling job retry');

  const timeout = setTimeout(async () => {
    pendingRetries.delete(jobId);
    await executeRetry(jobId, reason);
  }, delayMs);

  pendingRetries.set(jobId, timeout);
}

/**
 * Cancel pending retry
 */
export function cancelPendingRetry(jobId: string): boolean {
  const timeout = pendingRetries.get(jobId);
  if (timeout) {
    clearTimeout(timeout);
    pendingRetries.delete(jobId);
    return true;
  }
  return false;
}

/**
 * Execute retry
 */
async function executeRetry(jobId: string, reason: string): Promise<void> {
  const dispatcher = getJobDispatcherService();

  logger.info({ jobId, reason }, 'Executing scheduled retry');

  const result = await dispatcher.retry(jobId);

  if (result) {
    logger.info({ jobId, newJobId: result.jobId }, 'Retry dispatched');
  } else {
    logger.warn({ jobId }, 'Retry failed - job not found or invalid state');
  }
}

/**
 * Handle approval completion - retry if configured
 */
export async function onApprovalCompleted(
  approvalId: string,
  approved: boolean,
  metadata?: Record<string, unknown>
): Promise<void> {
  if (!approved) return;

  const onApprove = metadata?.onApprove as string;
  const sourceJobId = metadata?.sourceJobId as string;

  if (!sourceJobId) return;

  switch (onApprove) {
    case 'retry_job':
      // Delay to allow resource activation
      scheduleRetry(sourceJobId, 1000, 'resource_created');
      break;

    case 'grant_and_retry':
      // Permission granted, retry
      scheduleRetry(sourceJobId, 500, 'permission_granted');
      break;
  }
}

/**
 * Calculate retry delay with backoff
 */
export function calculateRetryDelay(
  retryCount: number,
  config: RetryConfig = DEFAULT_RETRY_CONFIG
): number {
  return config.delayMs * Math.pow(config.backoffMultiplier, retryCount);
}

/**
 * Check if error is retryable
 */
export function isRetryableError(
  errorCode: string,
  config: RetryConfig = DEFAULT_RETRY_CONFIG
): boolean {
  return config.retryOnErrors.includes(errorCode);
}

